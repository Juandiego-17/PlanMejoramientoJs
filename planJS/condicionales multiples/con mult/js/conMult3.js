let comida = 2;

switch (comida) {
    case 1:
        console.log("Es hora de desayunar");
        break;
    case 2:
        console.log("Es hora de almorzar");
        break;
    case 3:
        console.log("Es hora de cenar");
        break;
    default:
        console.log("Opción no válida.");
}
